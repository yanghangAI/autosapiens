"""BEDLAM2 dataset for RGBD 3D pose estimation.

Each sample is one person in one video frame.  The index is
``(label_path, body_idx, frame_idx)`` so multi-person sequences are
fully supported.

The class inherits from ``BaseDataset`` (not ``BaseCocoStyleDataset``)
because BEDLAM2 annotations are NPZ files, not COCO JSON.

Usage in config::

    dataset = dict(
        type='Bedlam2Dataset',
        data_root='/path/to/BEDLAM2',
        seq_paths_file='/path/to/train_seqs.txt',   # one rel path per line
        pipeline=[...],
    )

    # Or pass seq_paths directly:
    dataset = dict(
        type='Bedlam2Dataset',
        data_root='/path/to/BEDLAM2',
        seq_paths=['folder/seq1.npz', 'folder/seq2.npz', ...],
        pipeline=[...],
    )
"""

from __future__ import annotations

import os
from pathlib import Path
from typing import List, Optional

import numpy as np
from mmengine.dataset import BaseDataset

from mmpose.registry import DATASETS


@DATASETS.register_module()
class Bedlam2Dataset(BaseDataset):
    """BEDLAM2 frame-level dataset for RGBD 3D pose estimation.

    Builds a flat index of ``(label_path, body_idx, frame_idx)`` triples
    from a list of sequence NPZ files.  Actual data loading (RGB frame,
    depth map, joints, K matrix) is deferred to the ``LoadBedlamLabels``
    transform.

    Args:
        data_root (str): Absolute path to the BEDLAM2 data root directory
            (contains ``data/label/``, ``data/frames/``, etc.).
        seq_paths (list[str], optional): List of relative sequence paths
            like ``"folder/seq.npz"``, relative to ``data_root/data/label/``.
            Mutually exclusive with ``seq_paths_file``.
        seq_paths_file (str, optional): Path to a text file containing one
            relative sequence path per line (generated by
            ``tools/dataset_converters/generate_bedlam2_splits.py``).
            Mutually exclusive with ``seq_paths``.
        frame_stride (int): Step between sampled frames (default 5 → 6 fps
            from 30 fps source).
        max_seqs (int, optional): Truncate to this many sequences. Useful
            for quick smoke-tests to avoid indexing the full dataset.
        pipeline (list): Data transform pipeline.
        metainfo (dict): Dataset metainfo (from
            ``configs/_base_/datasets/bedlam2_smplx70.py``).
        test_mode (bool): Whether in test/val mode.
        max_refetch (int): Maximum retries when a transform returns ``None``.
            Used by MMEngine's ``prepare_data`` for OOB / tiny-bbox samples.
    """

    METAINFO: dict = dict(
        from_file='configs/_base_/datasets/bedlam2_smplx70.py')

    def __init__(
        self,
        data_root: str,
        seq_paths: Optional[List[str]] = None,
        seq_paths_file: Optional[str] = None,
        frame_stride: int = 5,
        max_seqs: Optional[int] = None,
        pipeline: list = [],
        metainfo: Optional[dict] = None,
        test_mode: bool = False,
        max_refetch: int = 10,
        **kwargs,
    ):
        if seq_paths is not None and seq_paths_file is not None:
            raise ValueError('Provide either seq_paths or seq_paths_file, not both.')
        if seq_paths is None and seq_paths_file is None:
            raise ValueError('One of seq_paths or seq_paths_file is required.')

        self._data_root = data_root
        self._frame_stride = frame_stride

        # Resolve sequence paths
        if seq_paths_file is not None:
            with open(seq_paths_file) as f:
                seq_paths = [ln.strip() for ln in f if ln.strip()]
        if max_seqs is not None:
            seq_paths = seq_paths[:max_seqs]
        self._seq_paths = seq_paths

        super().__init__(
            data_root=data_root,
            pipeline=pipeline,
            metainfo=metainfo,
            test_mode=test_mode,
            max_refetch=max_refetch,
            **kwargs,
        )

    def load_data_list(self) -> List[dict]:
        """Build the flat sample index from NPZ label files.

        Returns:
            List of dicts, one per ``(label_path, body_idx, frame_idx)``
            triple.  Each dict contains all keys needed by
            ``LoadBedlamLabels``.
        """
        data_list = []
        label_root = os.path.join(self._data_root, 'data', 'label')
        depth_npy_root = os.path.join(self._data_root, 'data', 'depth', 'npy')
        depth_npz_root = os.path.join(self._data_root, 'data', 'depth', 'npz')
        frames_root = os.path.join(self._data_root, 'data', 'frames')

        sample_id = 0
        for seq_rel in self._seq_paths:
            label_path = os.path.join(label_root, seq_rel)
            try:
                with np.load(label_path, allow_pickle=True) as meta:
                    n_frames_raw = int(meta['n_frames'])
                    n_body = int(meta['joints_cam'].shape[0])
                    folder_name = str(meta['folder_name'])
                    seq_name = str(meta['seq_name'])
            except Exception as e:
                raise RuntimeError(
                    f'Failed to read label {label_path}: {e}') from e

            # Frame indices after downsampling
            frame_indices = list(range(0, n_frames_raw, self._frame_stride))

            depth_npy_path = os.path.join(
                depth_npy_root, folder_name, f'{seq_name}.npy')
            depth_npz_path = os.path.join(
                depth_npz_root, folder_name, f'{seq_name}.npz')

            for body_idx in range(n_body):
                for frame_idx in frame_indices:
                    img_path = os.path.join(
                        frames_root, folder_name, seq_name,
                        f'{frame_idx:05d}.jpg')

                    data_list.append(dict(
                        label_path=label_path,
                        body_idx=body_idx,
                        frame_idx=frame_idx,
                        img_path=img_path,
                        depth_npy_path=depth_npy_path,
                        depth_npz_path=depth_npz_path,
                        folder_name=folder_name,
                        seq_name=seq_name,
                        id=sample_id,
                        img_id=sample_id,
                    ))
                    sample_id += 1

        return data_list
